package searchalgorithms.searchclasses;

//parallelsum parent class file

public class ParallelSum {
    // parallel sum class

    public int parallel_sum(int[] my_array1, int[] my_array2, int index) {
        return my_array1[index] + my_array2[index];
    }

    public float parallel_sum(float[] my_array1, float[] my_array2, int index) {
        return my_array1[index] + my_array2[index];
    }
}